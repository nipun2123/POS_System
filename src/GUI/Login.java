    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked

        String name = username.getText();

        if (!name.isEmpty()) {

            try {
                ResultSet res = UserManager.getDeactiveUserNameFromName(name);
                if (!res.next()) {
                    
                    if (UserManager.isUserNameExits(name)) {
                        loadQuestions(name);
                        fogetPassword.pack();
                        fogetPassword.setLocationRelativeTo(null);
                        fogetPassword.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, WRONGUSERNAMEERROR, "අවවාදයයි", JOptionPane.WARNING_MESSAGE);
                    }
                    
                } else {
                    JOptionPane.showMessageDialog(this, ACCOUNTDEACTIVATEDMSG, "දැනුම්දීමයි", JOptionPane.WARNING_MESSAGE);
                    username.grabFocus();
                    username.selectAll();
                    password.setText(null);
                }
            } catch (SQLException ex) {
               ex.printStackTrace();
            }

        } else {
            JOptionPane.showMessageDialog(this, NOUSERNAMEMSG, "අවවාදයයි", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jLabel1MouseClicked

