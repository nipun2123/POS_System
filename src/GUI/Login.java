    private int counter = 0;

    private String uName;
    private String a1, a2, a3;

    private void login() {

        try {

            Loggers.Log4JTest.exceptionLogger();
            Logger logger = Logger.getLogger("Login");

            String usernameTxt = username.getText();
            String passwordTxt = Encription.Encript.encript(String.valueOf(password.getPassword()));

            ResultSet result = UserManager.getActiveUserNameFromName(usernameTxt);

            if (result.next()) {
                counter++;

                ResultSet rs = UserManager.getActiveUserFromCredential(usernameTxt, passwordTxt);

                if (rs.next()) {
                    long currentTime = new java.util.Date().getTime();
                    java.sql.Timestamp currentStamp = new java.sql.Timestamp(currentTime);

                    UserManager.addLoginLog(rs.getString("iduser"), currentStamp);

                    logger.info(usernameTxt + " loging to system");
//                    new Main().setVisible(true);
                    new Loading(rs.getString("iduser")).setVisible(true);
                    this.dispose();

                } else {
                    logger.warn(usernameTxt + " trying to login with password " + Encription.Encript.decript(passwordTxt));
                    JOptionPane.showMessageDialog(this, WRONGPASSWORDERROR, "වැරදි", JOptionPane.WARNING_MESSAGE);
                    password.grabFocus();
                    password.selectAll();
                    if (counter == 3) {
                        UserManager.deactivateUserFromUsername(usernameTxt);
                        logger.info(usernameTxt + " user account has been deactivated!");
                        JOptionPane.showMessageDialog(this, THREEATTEMPSFAILED, "අවවාදයයි", JOptionPane.WARNING_MESSAGE);
                        dispose();
                    }

                }
            } else {
                ResultSet res = UserManager.getDeactiveUserNameFromName(usernameTxt);
                if (res.next()) {
                    JOptionPane.showMessageDialog(this, ACCOUNTDEACTIVATEDMSG, "දැනුම්දීමයි", JOptionPane.WARNING_MESSAGE);
                    logger.info(res.getString("username") + " user account has been deactivated!");
                    username.grabFocus();
                    username.selectAll();
                    password.setText(null);
                } else {
                    logger.warn("Someone trying to login with username " + usernameTxt);
                    JOptionPane.showMessageDialog(this, WRONGUSERNAMEERROR, "වැරදි", JOptionPane.WARNING_MESSAGE);
                    username.grabFocus();
                    username.selectAll();
                    password.setText(null);
                }
            }
        } catch (Exception e) {
            DB.processException(e);
        }
    }

    private void loadQuestions(String username) {

        try {
            uName = username;
            ResultSet result = UserManager.getSelecteQ_AFromUsername(username);

            while (result.next()) {
                if (result.getRow() == 1) {
                    question1.setText(result.getString("recovery_question"));
                    a1 = result.getString("answer");
                } else if (result.getRow() == 2) {
                    question2.setText(result.getString("recovery_question"));
                    a2 = result.getString("answer");
                } else if (result.getRow() == 3) {
                    question3.setText(result.getString("recovery_question"));
                    a3 = result.getString("answer");
                }
            }

        } catch (SQLException ex) {
            DB.processException(ex);
        }
    }

